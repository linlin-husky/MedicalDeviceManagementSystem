    public void populateTable() {

        DefaultTableModel model = (DefaultTableModel) tblBundleRequests.getModel();
        model.setRowCount(0); 

        if (organization == null || organization.getWorkQueue() == null) {
            System.err.println("PHSAdminOrganization or its WorkQueue is null. Cannot populate requests.");
            return;
        }


        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {

            if (request instanceof BundleReservationWorkRequest) {
                BundleReservationWorkRequest bundleRequest = (BundleReservationWorkRequest) request;

                Object[] row = new Object[7];

                row[0] = bundleRequest;
                row[1] = bundleRequest.getPatientName();
                row[2] = bundleRequest.getPatientID();
                row[3] = bundleRequest.getSurgeryType();
                row[4] = bundleRequest.getRequiredDate();
                row[5] = getDeviceInfoFromRequest(bundleRequest);
                row[6] = bundleRequest.getStatus(); 

                model.addRow(row);
            }
        }
    }
    
    private String getDeviceInfoFromRequest(BundleReservationWorkRequest request) {
        if (request == null) {
            return "None";
        }

        StringBuilder devices = new StringBuilder();

        boolean daVinciSelected = false;

        if (request.getResourceBundle() != null && 
            request.getResourceBundle().getRequiredDevices() != null) {

            for (Device device : request.getResourceBundle().getRequiredDevices()) {
                if (device != null && device.getName().toLowerCase().contains("davinci")) {
                    daVinciSelected = true;
                    break;
                }
            }

            if (daVinciSelected) {
                devices.append("DaVinci Console, DaVinci Vision Cart, DaVinci Arms Set");
            }

            for (Device device : request.getResourceBundle().getRequiredDevices()) {
                if (device != null && !device.getName().toLowerCase().contains("davinci")) {
                    if (devices.length() > 0) {
                        devices.append(", ");
                    }
                    devices.append(device.getName());
                }
            }
        }

        return devices.length() > 0 ? devices.toString() : "None";
    }

    private void hideTableColumn(javax.swing.JTable table, int columnIndex) {
        if (table != null && table.getColumnModel().getColumnCount() > columnIndex && columnIndex >= 0) {
           TableColumn column = table.getColumnModel().getColumn(columnIndex);
           column.setMinWidth(0);
           column.setMaxWidth(0);
           column.setWidth(0);
           column.setPreferredWidth(0);
        } else {
            System.err.println("Error hiding column " + columnIndex + " for table " + (table != null ? table.getName() : "null"));
        }
    }
    
