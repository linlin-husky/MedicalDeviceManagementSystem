    private void btnProcessRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessRequestActionPerformed

        int selectedRowIndex = tblBundleRequests.getSelectedRow();

        if (selectedRowIndex < 0) {

            JOptionPane.showMessageDialog(this, "Please select a request from the table first.", "No Request Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        BundleReservationWorkRequest requestToProcess = null;
        Object requestObject = tblBundleRequests.getValueAt(selectedRowIndex, 0);

        if (requestObject instanceof BundleReservationWorkRequest) {
             requestToProcess = (BundleReservationWorkRequest) requestObject;
        } else {

             JOptionPane.showMessageDialog(this, "Error: Could not retrieve valid request data from the selected row.", "Data Error", JOptionPane.ERROR_MESSAGE);
             System.err.println("Object in hidden column is not a BundleReservationWorkRequest or is null.");
             return;
        }

        String currentStatus = requestToProcess.getStatusString();
        if (!"Submitted to PHS".equalsIgnoreCase(currentStatus)) {
            JOptionPane.showMessageDialog(this, "This request is currently in '" + currentStatus + "' status and cannot be processed now.", "Request Status", JOptionPane.INFORMATION_MESSAGE);
            return; 
        }

        CreateResourceBundlesJPanel createResourceBundlesJPanel = new CreateResourceBundlesJPanel(
                userProcessContainer, 
                userAccount,
                enterprise,
                business,
                requestToProcess
        );

        userProcessContainer.add("CreateResourceBundlesJPanel_Process", createResourceBundlesJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnProcessRequestActionPerformed

   

