    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        model.setRowCount(0);
        
        // Add custom renderer
        workRequestJTable.setDefaultRenderer(Object.class, new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(
                    javax.swing.JTable table, Object value, boolean isSelected, 
                    boolean hasFocus, int row, int column) {
                
                java.awt.Component c = super.getTableCellRendererComponent(
                        table, value, isSelected, hasFocus, row, column);
                
                // Get the room availability from the third column (index 2)
                Boolean isAvailable = (Boolean) table.getModel().getValueAt(row, 6);
                
                // Set background color based on availability
                if (isAvailable != null && !isAvailable) {
                    c.setBackground(new java.awt.Color(255, 200, 200)); // Light red
                } else {
                    c.setBackground(table.getBackground());
                }
                
                return c;
            }
        });
        
        DeviceDirectory deviceDirectory = deviceManagementOrganization.getDeviceDirectory();
        for (Device  device: deviceDirectory.getDeviceList()) {
            Object[] row = new Object[7];
            row[0] = device;
            row[1] = device.getName();
            row[2] = device.getType();
            row[3] = device.getManufacturer();
            row[4] = device.getCurrentLocation(); 
            row[5] = device.getIsPortable();     
            row[6] = device.isAvailable();
            model.addRow(row);
        }
    }

