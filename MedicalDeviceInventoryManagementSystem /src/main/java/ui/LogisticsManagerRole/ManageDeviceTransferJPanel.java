    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
       
         int selectedRow = tblWorkRequests.getSelectedRow();
        if (selectedRow >= 0) {
            DeviceTransferWorkRequest selectedRequest = ( DeviceTransferWorkRequest ) tblWorkRequests.getValueAt(selectedRow, 0);
            logisticsOrganization.transferDevice(selectedRequest);
              JOptionPane.showMessageDialog(null, "You have transferred the device to customer.", "Order Placed", JOptionPane.INFORMATION_MESSAGE);

            

        } else {
            JOptionPane.showMessageDialog(null, "Choose a logistics company to process.");
            return;
        }
    
    }//GEN-LAST:event_btnProcessActionPerformed

