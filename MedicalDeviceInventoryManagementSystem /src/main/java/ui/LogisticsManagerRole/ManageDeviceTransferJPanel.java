      public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkRequests.getModel();

        model.setRowCount(0);
        
        
         for(WorkRequest request : logisticsOrganization.getWorkQueue().getWorkRequestList()){
            
         if (request instanceof DeviceTransferWorkRequest) {
            DeviceTransferWorkRequest deviceRequest = (DeviceTransferWorkRequest) request;
            Object[] row = new Object[5];
            row[0] = deviceRequest;
            row[1] = deviceRequest.getSender() == null ? null : request.getSender().getEmployee().getName();
            row[2] = deviceRequest.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = deviceRequest.getDmo();
            row[4] = deviceRequest.getStatus();
     
                
            model.addRow(row);
         }
        }
      }

