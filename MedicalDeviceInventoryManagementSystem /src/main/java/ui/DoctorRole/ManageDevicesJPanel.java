    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[9];
            row[0] = request;
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            
            String deviceName = ((DeviceReservationWorkRequest) request).getDeviceName();
            String duration = ((DeviceReservationWorkRequest) request).getDuration();
            String date = ((DeviceReservationWorkRequest) request).getScheduleDate();
            Integer reserveID = ((DeviceReservationWorkRequest) request).getId();
            row[3] = deviceName == null ? "" : deviceName;
            row[4] = duration == null ? "" : duration;
            row[5] = date == null ? "" : date;
            
            Integer deviceAcquisitionID = ((DeviceReservationWorkRequest) request).getDeviceAcquisationID();
            String deviceID = ((DeviceReservationWorkRequest) request).getDeviceID();
            row[6] = (reserveID != null && reserveID != 0) ? reserveID : "N/A";
            row[7] = (deviceAcquisitionID != null && deviceAcquisitionID != 0) ? deviceAcquisitionID : "N/A";
            //row[8] = (deviceID != null && deviceID != 0) ? deviceID : "N/A";
            row[8] = (deviceID != null && !deviceID.isEmpty() && !"0".equals(deviceID)) ? deviceID : "N/A";
//            row[6] = ((DeviceReservationWorkRequest) request).getDeviceAcquisationID();
//            row[7] = ((DeviceReservationWorkRequest) request).getDeviceID();
            
            model.addRow(row);
        }
    }

