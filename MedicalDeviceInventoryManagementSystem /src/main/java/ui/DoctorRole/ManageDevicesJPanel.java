    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int row = workRequestJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warrning", JOptionPane.WARNING_MESSAGE);
            return;
        }
         WorkRequest selectedRequest = (WorkRequest) workRequestJTable.getValueAt(row, 0);
         
         userAccount.getWorkQueue().getWorkRequestList().remove(selectedRequest);
         populateRequestTable();
         
        Organization org = null;
        for (Organization organization1 : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization1 instanceof DeviceManagementOrganization){
                org = organization1;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().remove(selectedRequest);
            
        }
     
        
    }//GEN-LAST:event_btnDeleteActionPerformed


