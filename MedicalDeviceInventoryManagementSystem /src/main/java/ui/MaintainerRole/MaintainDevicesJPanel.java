    private void requestOperationsRoomJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestOperationsRoomJButtonActionPerformed
 
        int selectedRow = workRequestJTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            Device selectedDevice = (Device) workRequestJTable.getValueAt(selectedRow, 0);    
            
            if(selectedDevice == null) {
                JOptionPane.showMessageDialog(null, "Invalid device selection", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("UpdateDeviceJPanel", new UpdateDeviceJPanel(userProcessContainer, selectedDevice));
            layout.next(userProcessContainer);
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select a book from the list view.", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_requestOperationsRoomJButtonActionPerformed

