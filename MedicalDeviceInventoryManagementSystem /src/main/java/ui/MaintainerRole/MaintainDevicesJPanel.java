    public void populateDevicesTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        
        // Add this custom renderer to color rows
        workRequestJTable.setDefaultRenderer(Object.class, new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                java.awt.Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String lastMaintenanceStr = (String) table.getValueAt(row, 7); // Last maintenance column
                
                if(lastMaintenanceStr == null || lastMaintenanceStr.equals("N/A")) {
                    c.setBackground(table.getBackground());
                    return c;
                }
                
                try {
                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
                    java.util.Date lastMaintenance = sdf.parse(lastMaintenanceStr);
                    java.util.Date now = new java.util.Date();
                    long diffInMillies = now.getTime() - lastMaintenance.getTime();
                    long diffInMonths = diffInMillies / (30L * 24L * 60L * 60L * 1000L);
                    
                    if (diffInMonths >= 6) {
                        c.setBackground(java.awt.Color.RED);
                    } else if (diffInMonths >= 4) {
                        c.setBackground(java.awt.Color.ORANGE);
                    } else if (diffInMonths >= 2) {
                        c.setBackground(java.awt.Color.YELLOW);
                    } else {
                        c.setBackground(table.getBackground());
                    }
                } catch (java.text.ParseException e) {
                    c.setBackground(table.getBackground());
                }
                return c;
            }
        });

        DeviceDirectory deviceDirectory = ((HospitalEnterprise) enterprise).getDeviceDirectory();
        
        for (Device device : deviceDirectory.getDeviceList()) {
            Object[] row = new Object[8];
            row[0] = device;
            row[1] = device.getName();
            row[2] = device.getType();
            row[3] = device.getManifacturer();
            row[4] = device.getCurrentLocation();
            row[5] = device.getIsPortable();
            row[6] = device.isAvailable();
            row[7] = device.getLastMaintenanceDate() != null ? device.getLastMaintenanceDate() : "N/A";
            
            model.addRow(row);
        }
    }

